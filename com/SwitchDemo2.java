package javasessions.com;

public class SwitchDemo2 {

	public static void main(String[] args) {
		int num=5;
		switch(num)
		{
		
				case 1: 
		
					System.out.println("result of 1 is 100");
					break;
				case 2: 
					System.out.println("result of 2 is 80");
					break;
				case 3: 
					
					System.out.println("result of 3 is 65");
					break;
				case 4: 
					
					System.out.println("result of 4 is 35");
					break;
		
				case 5: 
					
					System.out.println("result of 5 is 95");
					break;
		
				default:
					System.out.println("Plz pass correct usn number");
					break;
		
		
		
		
		}
		
			System.out.println("Thanks");
		
		


	}

}
